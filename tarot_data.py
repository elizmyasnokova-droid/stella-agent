def draw_cards(count: int = 1, positions: list = None) -> list:
    """Вытянуть карты таро."""
    deck = list(range(78))
    random.shuffle(deck)
    drawn = deck[:count]

    result = []
    for i, card_num in enumerate(drawn):
        reversed_card = random.random() < 0.3  # 30% шанс перевёрнутой
        position = positions[i] if positions and i < len(positions) else f"Карта {i+1}"

        if card_num < 22:
            card = MAJOR_ARCANA[card_num].copy()
            card["type"] = "Старший Аркан"
        else:
            suit_num = (card_num - 22) // 14
            card_in_suit = (card_num - 22) % 14
            suits_list = list(SUITS.keys())
            suit_name = suits_list[suit_num]
            values = ["Туз", "Двойка", "Тройка", "Четвёрка", "Пятёрка",
                     "Шестёрка", "Семёрка", "Восьмёрка", "Девятка", "Десятка",
                     "Паж", "Рыцарь", "Королева", "Король"]
            value = values[card_in_suit]
            meanings = MINOR_ARCANA_MEANINGS[value]
            suit_info = SUITS[suit_name]
            card = {
                "name": f"{value} {suit_name}",
                "type": f"Младший Аркан — {suit_name}",
                "element": suit_info["element"],
                "theme": suit_info["theme"],
                "upright": meanings["upright"],
                "reversed": meanings["reversed"],
                "astro": f"Стихия {suit_info['element']}",
            }

        card["position"] = position
        card["is_reversed"] = reversed_card
        card["meaning"] = card["reversed"] if reversed_card else card["upright"]
        card["orientation"] = "🔄 Перевёрнутая" if reversed_card else "↑ Прямая"
        result.append(card)

    return result


